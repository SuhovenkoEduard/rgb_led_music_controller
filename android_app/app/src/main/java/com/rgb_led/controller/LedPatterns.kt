/*
 * 38020 Hz is frequency of infrared blaster
 * 1 (second) / 38020 = 2.6e-5 or 26 microseconds
 */
fun convertPatternToIntArrayOfMicroseconds(pattern: String): IntArray {
    return pattern
        .split(" ")
        .map { number -> "0x${number}" }
        .map { number -> Integer.decode(number) }
        .drop(4)
        .map { number -> number * 26 }
        .toIntArray()
}